    //   })
    //   .min(1, { message: "Number of sessions must be at least 1" }),
    addPersonaAutomatically: z.boolean().default(true),
    // columns: z.array(
    //   z.object({
    //     name: z
    //       .string()
    //       .min(1, "Column name is required")
    //       .max(50, "Column name must be less than 50 characters"),

    //     type: z.enum(
    //       ["text", "boolean", "integer", "float", "json", "array", "datetime"],
    //       { message: "Data type is required" },
    //     ),

    //     description: z
    //       .string()
    //       .min(1, "Description is required")
    //       .max(200, "Description must be less than 200 characters"),
    //   }),
    // ),
    personas: z
      .array(z.object({ id: z.string() }))
      .transform((arr) => arr.map((item) => item.id)),
    hasAgentPrompt: z.boolean().default(false),
    agentDefExists: z.boolean().default(false),
