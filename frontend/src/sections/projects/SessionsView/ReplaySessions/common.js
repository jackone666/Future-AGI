const generateSimulationNameAndDescription = (projectDetail) => {
  return {
    name: `${projectDetail?.name}_replay_${format(new Date(), "dd-MMM-yyyy_HH-mm")}`,
    description: `Generated by Replay Sessions for project ${projectDetail?.name}`,
  };
};

const transformMapping = (mapping, requiredKeys, optionalKeys) => {
  const requiredKeysMappingObject = requiredKeys?.reduce((acc, curr) => {
    return {
      ...acc,
      [curr.replace(/\./g, "_")]: curr,
    };
  }, {});
  const optionalKeysMappingObject = optionalKeys?.reduce((acc, curr) => {
    return {
      ...acc,
      [curr.replace(/\./g, "_")]: curr,
    };
  }, {});
  const newMapping = {};

  // Process all keys in the mapping object
  // Check if they match required keys or optional keys
  Object.entries(mapping).forEach(([transformedKey, value]) => {
    // Check if it's a required key
    if (requiredKeysMappingObject[transformedKey]) {
      newMapping[requiredKeysMappingObject[transformedKey]] = value;
    }
    // Check if it's an optional key
    else if (optionalKeysMappingObject[transformedKey]) {
      newMapping[optionalKeysMappingObject[transformedKey]] = value;
    }
  });

  // Remove empty values
  const keysToBeRemoved = [];
  Object.entries(newMapping).forEach(([key, val]) => {
    if (val === "" || val === null || val === undefined) {
      keysToBeRemoved.push(key);
    }
  });
  for (let i = 0; i < keysToBeRemoved.length; ++i) {
    delete newMapping[keysToBeRemoved[i]];
  }
  return newMapping;
};

const getEvaluationMappingPayload = (
  data,
  requiredKeys,
  optionalKeys,
  projectEvals,
) => {
  const globalMapping = transformMapping(
    data.config.mapping,
    requiredKeys,
    optionalKeys,
  );
  const globalModel = data.model || "";
  const globalKbId = data.kbId || "";
  const globalErrorLocalizer = data.errorLocalizer || false;

  // Create individual eval payloads
  const individualEvalPayloads = projectEvals.map((evalItem) => {
    // Filter mapping to only include keys relevant to this eval
    const evalMapping = {};
    const evalRequiredKeys = evalItem.requiredKeys || [];
    const evalOptionalKeys = evalItem.optionalKeys || [];

    // Add required keys mapping
    evalRequiredKeys.forEach((key) => {
      if (globalMapping[key]) {
        evalMapping[key] = globalMapping[key];
      }
    });

    // Add optional keys mapping
    evalOptionalKeys.forEach((key) => {
      if (globalMapping[key]) {
        evalMapping[key] = globalMapping[key];
      }
    });

    // Determine model for this eval
    let evalModel = globalModel;
    if (evalItem.tags?.includes("FUTURE_EVALS")) {
      // Use global model if it's in the eval's allowed models
      const allowedEvalModels = evalItem.models || [];
      if (
        !allowedEvalModels.includes(globalModel) &&
        allowedEvalModels.length > 0
      ) {
        evalModel = allowedEvalModels[0]; // Fallback to first allowed model
      }
    } else {
      evalModel = ""; // No model needed for non-FUTURE_EVALS
    }

    return {
      name: evalItem.name,
      templateId: evalItem.evalTemplateId,
      mapping: evalMapping,
      model: evalModel || undefined,
      errorLocalizer: globalErrorLocalizer,
      kbId: globalKbId || undefined,
      config: {
        mapping: evalMapping,
        config: {},
        reasonColumn: data.config.reasonColumn,
      },
    };
  });
  return individualEvalPayloads;
};

export const getRunSimulationPayload = (
  data,
  projectDetail,
  requiredKeys,
  optionalKeys,
  projectEvals,
) => {
  const { name, description } =
    generateSimulationNameAndDescription(projectDetail);
  const evaluationsConfig =
    projectEvals?.length > 0
      ? getEvaluationMappingPayload(
          data,
          requiredKeys,
          optionalKeys,
          projectEvals,
        )
      : [];
  return {
    agent_definition_id: data?.createdScenario?.agentDefinitionId,
    agent_version: data?.createdScenario?.agentDefinitionLatestVersionId,
    name: name,
    description: description,
    enable_tool_evaluation: false,
    scenario_ids: [data?.createdScenario?.scenarioId],
    eval_config_ids: [],
    evaluations_config: evaluationsConfig,
    replay_session_id: data?.replaySessionId,
  };
};
export const getConfirmationMessages = (status) => {
  if (status === SCENARIO_STATUS.PROCESSING) {
    return {
      title: "Are you sure you want to close?",
      message:
        "Your scenario is currently being processed. Are you sure you want to leave?",
      content:
        "The scenarios will continue generating in the background. You can run the simulation later.",
    };
  }

  // Default messages for other statuses (Completed, Failed, etc.) or when status is unknown
  return {
    title: "Unsaved Changes",
    message: "Are you sure you want to proceed?",
    content:
      "You have unsaved changes. If you leave now, they will be permanently lost.",
  };
};

export const buildEvaluationMappingFormSchema = (requiredKeys) => {
  return z.object({
    model: z.string(),
    config: z.object({
      mapping: z
        .record(z.string().optional())
        .refine(
          (val) =>
            requiredKeys.every((key) => val[key] && val[key].trim() !== ""),
          {
            message: "Required input mappings must be filled",
          },
        ),
      reasonColumn: z.boolean(),
    }),
    errorLocalizer: z.boolean(),
    kbId: z.string().optional(),
    projectEvals: z.array(z.unknown()).default([]),
  });
};

export const formatProjectEvals = (evalConfigs) => {
  if (!evalConfigs) return [];
  return evalConfigs?.map((evalItem) => ({
    evalTemplateId: evalItem.evalTemplate?.id,
    name: evalItem.name,
    description: evalItem.evalTemplate?.description,
    requiredKeys: evalItem.evalTemplate?.requiredKeys,
    optionalKeys: evalItem.evalTemplate?.optionalKeys,
    models: evalItem?.availableModels ?? [],
    tags: evalItem.evalTemplate?.tags ?? [],
    fromProject: true,
  }));
};
